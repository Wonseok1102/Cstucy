#include<stdio.h>

int main(void) {
	char a = 'a';
	char b = 'b';
	char c = 'A';
	char d = '!';
	char e = '@';

	printf("변수 a의 ASCII코드값: %d, 문자: %c를 의미\n", a, a);
	printf("변수 b의 ASCII코드값: %d, 문자: %c를 의미\n", b, b);
	printf("변수 c의 ASCII코드값: %d, 문자: %c를 의미\n", c, c);
	printf("변수 d의 ASCII코드값: %d, 문자: %c를 의미\n", d, d);
	printf("변수 e의 ASCII코드값: %d, 문자: %c를 의미\n", e, e);

	return 0;
}